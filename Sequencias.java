public class Sequencias {
    public static void main(String[] args) {
        
        int a = 7 + 2;
        System.out.println("a) Próximo elemento: " + a);

        
        int b = 64 * 2;
        System.out.println("b) Próximo elemento: " + b);

        
        int c = 7 * 7;
        System.out.println("c) Próximo elemento: " + c);

        
        int d = 8 * 8;
        System.out.println("d) Próximo elemento: " + d);

        
        int e = 5 + 8;
        System.out.println("e) Próximo elemento: " + e);

        
        int f = 19 + 1;
        System.out.println("f) Próximo elemento: " + f);
    }
}
